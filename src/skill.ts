import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'fs'
import { dirname, join } from 'path'
import type { DevSentinelConfig } from './types.js'

export type EditorTarget = 'claude' | 'opencode' | 'cursor' | 'windsurf' | 'copilot' | 'all'

export interface SkillResult {
  editor: string
  files: Array<{ path: string; content: string; description: string }>
}

function mcpServerBlock(port: number): Record<string, unknown> {
  return {
    devcontrol: {
      type: 'sse',
      url: `http://localhost:${port}/mcp`,
    },
  }
}

function buildClaudeSkills(projectRoot: string, config: DevSentinelConfig, mcpPort?: number): SkillResult {
  const files: SkillResult['files'] = []

  const commandContent = `---
description: SP-DevControl — governance, sessions, change control, compliance
allowed-tools: Bash
---
Run: devcontrol $ARGUMENTS
Use this to: start/close sessions, approve/reject changes, check policy, generate compliance reports.
Examples: /devcontrol session:start --objective "feat X" | /devcontrol project:check | /devcontrol report:compliance
`

  files.push({
    path: join(projectRoot, '.claude', 'commands', 'devcontrol.md'),
    content: commandContent,
    description: 'Claude Code slash command /devcontrol',
  })

  const settingsHook = {
    hooks: {
      PreToolUse: [
        {
          matcher: 'Bash',
          hooks: [
            {
              type: 'command',
              command: 'devcontrol policy:command',
            },
          ],
        },
      ],
    },
  }

  files.push({
    path: join(projectRoot, '.claude', 'settings.json'),
    content: JSON.stringify(settingsHook, null, 2),
    description: 'Claude Code settings with PreToolUse hook for policy enforcement',
  })

  if (mcpPort !== undefined) {
    const mcpConfig = {
      mcpServers: mcpServerBlock(mcpPort),
    }

    files.push({
      path: join(projectRoot, '.claude', 'mcp.json'),
      content: JSON.stringify(mcpConfig, null, 2),
      description: 'Claude Code MCP server configuration',
    })
  }

  return { editor: 'claude', files }
}

function buildOpencodeSkills(projectRoot: string, config: DevSentinelConfig, mcpPort?: number): SkillResult {
  const toolConfig: Record<string, unknown> = {
    tools: {
      devcontrol: {
        type: 'local',
        command: 'devcontrol',
        description: `SP-DevControl governance for ${config.project} — sessions, change control, compliance`,
      },
    },
  }

  if (mcpPort !== undefined) {
    toolConfig.mcpServers = mcpServerBlock(mcpPort)
  }

  return {
    editor: 'opencode',
    files: [
      {
        path: join(projectRoot, 'opencode.json'),
        content: JSON.stringify(toolConfig, null, 2),
        description: 'opencode tool integration for devcontrol CLI',
      },
    ],
  }
}

